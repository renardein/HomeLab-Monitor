function merge(lang) {
    const filePath = path.join(localesDir, `${lang}.json`);
    const obj = JSON.parse(fs.readFileSync(filePath, 'utf8'));
    const patch = PATCH[lang];
    if (!patch) throw new Error('No patch for ' + lang);
    for (const [k, v] of Object.entries(patch)) {
        if (Object.prototype.hasOwnProperty.call(obj, k)) {
            console.warn(`[${lang}] overwrite existing key: ${k}`);
        }
        obj[k] = v;
    }
    fs.writeFileSync(filePath, JSON.stringify(obj, null, 4) + '\n', 'utf8');
}

for (const lang of Object.keys(PATCH)) {
    merge(lang);
    console.log('Merged patch into', lang + '.json');
}
